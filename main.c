//Rotina chamada a cada vez que o ADC terminar a conversao
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	//HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_9);							//acionamento do pino GPIO para verificar frequencia e tempo de execucao
	ADC_DATA=HAL_ADC_GetValue(&hadc2);									//salva a leitura do ADC numa variavel
	x[1]=1.8+(ADC_DATA*3.3/255 -1.36)/2.2;								//calcula a posicao x em cm equivalente a tensão medida
	y[1]=x[1]-(0.9897*x[0])+(0.8969*y[0]);								//faz o calculo da funcao de transferencia discretizada
	//yb=y[1]*255/3.3;													//conversao do resultado para um valor em 8bits a ser enviado pelo DAC
	//HAL_DAC_SetValue(&hdac, DAC_CHANNEL_2, DAC_ALIGN_8B_R, yb);		//envio do sinal analogico pelo DAC
	pwm= 100-y[1]*100*5.7/11;											//calculo do duty cycle necessario para a corrente y[1]
	y[0]=y[1];															//salva os valores atuais para serem utilizados na proxima iteracao como valores antigos
	x[0]=x[1];
	__HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, pwm);					//atualiza o duty cycle do PWM gerado pelo canal 1 do TIM4 para o valor da variavel "pwm"
	//HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_9);							//pino GPIO desativado para verificar o tempo de execução
}
